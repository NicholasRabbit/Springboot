package com.springboot.config.entity;

//这里不用加 @Component注解，为了测试customer.xml的Spring的配置文件，和@ImportResouce的用法
public class Customer {

    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Customer{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
